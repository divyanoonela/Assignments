library(readr)
delivery_time<-read.csv(file.choose())
View(delivery_time)
attach(delivery_time)
library(moments)
hist(Delivery.Time)
hist(Sorting.Time)
skewness(Delivery.Time)
skewness(Sorting.Time)
qqnorm(Delivery.Time)
qqline(Delivery.Time)
qqnorm(Sorting.Time)
qqline(Sorting.Time)
boxplot(Delivery.Time)
boxplot(Sorting.Time)
colnames(delivery_time)
cor.test(Delivery.Time,Sorting.Time)
model1<-lm(Delivery.Time~Sorting.Time,data=delivery_time)
summary(model1)
model2<-lm(log(Delivery.Time)~Sorting.Time,data=delivery_time)
summary(model2)
library(caTools)
split<-sample.split(delivery_time,SplitRatio = 0.7)
train=subset(delivery_time,split==TRUE)
test=subset(delivery_time,split==FALSE)
model3<-lm(log(Delivery.Time)~Sorting.Time,data=train)
summary(model3)
errortrain<-train$Delivery.Time-model3$fitted.values
sum(errortrain)
rmsetrain<-sqrt(mean(errortrain^2))
pred_test<-predict(model3,newdata=test)
errortest<-test$Delivery.Time-pred_test
rmsetest<-sqrt(mean(errortest^2))
print(c(rmsetrain,rmsetest))
